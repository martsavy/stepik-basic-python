# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
def is_palindrome(text):
    flag = False
    text = text.replace(' ', '')
    text = text.replace(',', '')
    text = text.replace('.', '')
    text = text.replace('!', '')
    text = text.replace('?', '')
    text = text.replace('-', '')
    # text = text.translate({ord(i): None for i in ' .,!?-'})
    text = text.lower()
    text1 = text[::-1]
    if text == text1:
        flag = True
    return flag

