# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
from math import *
def solve(a, b, c):
    d = pow(b, 2) - 4 * a * c
    if d == 0:
        x1 = -b / (2 * a)
        x2 = -b / (2 * a)
    elif d > 0:
        x1 = (-b - sqrt(d)) / (2 * a)
        x2 = (-b + sqrt(d)) / (2 * a)
    return min(x1, x2), max(x1, x2)

# test
print(solve(1, -4, -5))
print(solve(-2, 7, -5))
print(solve(1, 2, 1))

# —Å—á–∏—Ç—ã–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ
a, b, c = int(input()), int(input()), int(input())

# –≤—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é
x1, x2 = solve(a, b, c)
print(x1, x2)